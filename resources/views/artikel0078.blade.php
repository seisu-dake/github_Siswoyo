    <p>Artificial Intelligence (AI) Vs Human Intelligence (HI), Siapa Pemenangnya?</p>
    <p>Smartphone, laptop, komputer, dan semua teknologi pada zaman sekarang  terbuat dari sebuah kecerdasan buatan yang dibuat oleh manusia. Melakukan semua yang kita perintahkan merupakan kelebihan Artificial Intelligence (AI) ini, tapi apakah kecerdasan buatan yang dibuat oleh manusia itu sendiri dapat mengalahkan kecerdasan manusia. So, mari kita bahas.
    Artificial Intelligence (AI), ilmu membuat mesin yang lebih pintar dan cerdas seperti manusia, telah memicu perdebatan yang tak terelakkan tentang Artificial Intelligence Vs Human Intelligence . 

    Memang, algoritme Machine Learning (ML) dan Deep Learning (DL) dibuat untuk membuat mesin belajar sendiri dan membuat keputusan seperti yang kita manusia lakukan. Dalam upaya untuk membuat mesin yang lebih pintar, Akankah Kecerdasan Manusia menghadapi krisis eksistensial?.</p>
    <p>Kecerdasan Buatan Vs Kecerdasan Manusia (AI vs HI)</p>
    <p>Visi membuat mesin yang dapat berpikir dan bertindak seperti manusia telah berevolusi dari fiksi film menjadi fakta dunia nyata. Ada bot, humanoids, robot, dan manusia digital yang mengalahkan manusia atau berkoordinasi dengan kita dalam banyak hal. </p>

    <p>Datang  perdebatan tentang Artificial Intelligence Vs Human Intelligence, pencapaian AI baru-baru ini meniru kecerdasan manusia lebih dekat dari sebelumnya namun, mesin masih jauh di luar kemampuan otak manusia. 

        Kemampuan manusia untuk menerapkan pengetahuan yang diperoleh dengan rasa logika, penalaran, pemahaman, pembelajaran dan pengalaman inilah yang membuat kita menonjol. 
        
        Meskipun Mesin mungkin dapat meniru perilaku manusia sampai batas tertentu, pengetahuan mereka mungkin berantakan saat membuat keputusan yang rasional seperti kita. 
        
        Mesin bertenaga AI membuat keputusan berdasarkan peristiwa dan hubungannya dengan mereka, namun, mereka kurang akal sehat. Sistem AI tidak mengerti dalam memahami "sebab" dan "akibat" . Sementara skenario dunia nyata membutuhkan pendekatan manusia yang holistik .</p>

